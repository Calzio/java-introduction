public class Javaif {

    public static void main(String[] args) {
        int a = 10;
        int b = 2;
        int maior;
        
        if (a > b) {
            maior = a;
        } else {
            maior = b;
        }
        
        System.out.println("O maior número é: " + maior);
    }
}
